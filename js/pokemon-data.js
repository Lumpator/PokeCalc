function getPokemonFile(id) {
    var pokemon = POKEMON[id];
    return pokemon ? "assets/" + pokemon.file : null;
}

function getPokemonName(id) {
    var pokemon = POKEMON[id];
    return pokemon ? pokemon.name : null;
}

function getPokemonGen(id) {
    var pokemon = POKEMON[id];
    return pokemon ? pokemon.gen : null;
}

function canEvolve(id) {
    return EVOLUTIONS.hasOwnProperty(id);
}

function getEvolution(id) {
    var evo = EVOLUTIONS[id];
    if (!evo) return null;
    if (Array.isArray(evo)) {
        return evo[Math.floor(Math.random() * evo.length)];
    }
    return evo;
}

function isMaxEvolution(id) {
    return !EVOLUTIONS.hasOwnProperty(id);
}

